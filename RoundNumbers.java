public class RoundNumbers{
	public static void main(String[] args) {

		double x = 75.3987;
        double y = 982.89764;
        
        // Printing x rounded to 2 decimal places
        System.out.printf("%.2f %n", x);
        
        // Printing y rounded to 2 decimal places
        System.out.printf("%.2f %n", y);
        
        // Printing x rounded to 3 decimal places
        System.out.printf("%.3f %n", x);
        
        // Printing y rounded to 3 decimal places
        System.out.printf("%.3f %n", y);

	}
}
