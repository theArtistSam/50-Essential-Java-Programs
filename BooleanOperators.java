public class BooleanOperators {
    public static void main(String[] args) {
        // Define the boolean variables
        boolean b1 = true;
        boolean b2 = false;
        
        // Perform boolean operation and assign the result to another variable
        boolean b3 = (b1 == b2);

        // Display the value of b3 using System.out.println()
        System.out.println("The value of b3 is: " + b3);
    }
}
