public class DataTypes{
	public static void main(String[] args) {
		System.out.println("Welcome To Java Class");

		/* Inorder to declare constants, we have to use the final data type to define it */

		byte byteInt = 2;
		int integer = 4554;
		short shortInt = 23;
		long longInt = 59835900323424l;
		float points = 45.65f;
		double doublePoints = 476.9548549;
		System.out.print("Here is your byte value ");
		System.out.println(byteInt);
		System.out.print("Here is your integer value ");
		System.out.println(integer);
		System.out.print("Here is your short integer value ");
		System.out.println(shortInt);
		System.out.print("Here is your long integer value ");
		System.out.println(longInt);
		System.out.print("Here is your decimal value with floats ");
		System.out.println(points);
		System.out.print("Here is your decimal value with floats that carries more point ");
		System.out.println(doublePoints);


	}
}